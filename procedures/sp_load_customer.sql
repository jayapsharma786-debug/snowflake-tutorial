CREATE OR REPLACE PROCEDURE SP_LOAD_CUSTOMER()
RETURNS STRING
LANGUAGE SQL
AS
$$
BEGIN

MERGE INTO DIM_CUSTOMER T
USING tables S
ON T.CUSTOMER_ID = S.CUSTOMER_ID

WHEN MATCHED THEN
UPDATE SET
    T.CUSTOMER_NAME = S.CUSTOMER_NAME,
    T.CITY = S.CITY

WHEN NOT MATCHED THEN
INSERT
(
 CUSTOMER_ID,
 CUSTOMER_NAME,
 CITY
)
VALUES
(
 S.CUSTOMER_ID,
 S.CUSTOMER_NAME,
 S.CITY
);

RETURN 'SUCCESS';

END;
$$;
